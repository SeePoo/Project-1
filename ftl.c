static void ssd_init_map_access_count(struct ssd *ssd)
{
    struct ssdparams *spp = &ssd->sp;

    ssd->moni.map_access_count = g_malloc0(sizeof(uint64_t) * spp->tt_pgs);
    for (uint64_t i = 0; i < spp->tt_pgs; i++) {
        ssd->moni.map_access_count[i] = 0;
    }
}

static int monitering_init(struct ssd *ssd) {
    struct s_monitering *moni = &ssd->moni;

    moni->IOPS = fopen("log_IOPS", "w+");
    if (moni->IOPS == NULL) {
        perror("fopen(\"log_IOPS\", \"w+\")");
        return 1;
    }
    moni->throughput = fopen("log_throughput", "w+");
    if (moni->throughput == NULL) {
        free(moni->IOPS);
        perror("fopen(\"log_throughput\", \"w+\")");
        return 2;
    }
    moni->GC = fopen("log_GC", "w+");
    if (moni->GC == NULL) {
        free(moni->IOPS);
        free(moni->throughput);
        perror("fopen(\"log_GC\", \"w+\")");
        return 3;
    }
    moni->WAF = fopen("log_WAF", "w+");
    if (moni->WAF == NULL) {
        free(moni->IOPS);
        free(moni->throughput);
        free(moni->GC);
        perror("fopen(\"log_WAF\", \"w+\")");
        return 4;
    }
    moni->CDF = fopen("log_CDF", "w+");
    if (moni->CDF == NULL) {
        free(moni->IOPS);
        free(moni->throughput);
        free(moni->GC);
        free(moni->WAF);
        perror("fopen(\"log_CDF\", \"w+\")");
        return 4;
    }

    moni->read_IO = 0;
    moni->write_IO = 0;
    moni->read_throughput = 0;
    moni->write_throughput = 0;

    moni->gc_erase_block_count = 0;

    moni->gc_data_size_for_WAF = 0;
    moni->req_size = 0;

    /* LPN 접근 횟수 배열 초기화 */
    ssd_init_map_access_count(ssd);

    moni->init_time = \
    moni->curr_time_1sec = \
    moni->prev_time_1sec = \
    moni->curr_time_10sec = \
    moni->prev_time_10sec = \
    qemu_clock_get_ns(QEMU_CLOCK_REALTIME);
    return 0;
}

